class Student():
    def __init__(self, apenom, email, address, city, phone_number, dni, date_born, create_at, code, active) -> None:
        self.apenom = apenom
        self.address = address
        self.city = city
        self.date_born = date_born
        self.phone_number = phone_number
        self.email = email
        self.create_at = create_at
        self.dni = dni
        self.code = code
        self.active = active